void main() {
  double sisiPersegi = 5;
  double alasSegitiga = 10;
  double tinggiSegitiga = 8;
  double jariLingkaran = 6;

  double luasPersegi = sisiPersegi * sisiPersegi;
  double luasSegitiga = 0.5 * alasSegitiga * tinggiSegitiga;
  double luasLingkaran = 3.14 * jariLingkaran * jariLingkaran;

  print("Luas persegi: $luasPersegi");
  print("Luas segitiga: $luasSegitiga");
  print("Luas lingkaran: $luasLingkaran");
}